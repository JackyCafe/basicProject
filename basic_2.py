# 運算子
a = 25
b = 2.2
c = 2
print(f' a+ b ={a+b}')
print(f' a- b ={a-b}')
print(f' a* b ={a*b}')
print (f' a/ b ={a/b}')
print (f' a/ b = %.2f'%(a/b))
print(f' a/ c ={a/c}')
print(f' a** c ={a**c}')
print(f' a // b ={a//b}')

